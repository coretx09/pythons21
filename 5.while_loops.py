def separateur():
    print("* " * 30)


# compteur
i = 0
while i <= 10:
    print(i)
    i += 2
separateur()

# decompteur
i = 5
while i >= 0:
    print(i)
    i -= 1
separateur()

# dessin
i = 1
while i <= 5:
    print("*" * i)
    i += 1

# calculatrice
i = 1
while i <= 9:
    print(f"{i} x 3 = {i * 3} ")
    i += 1

